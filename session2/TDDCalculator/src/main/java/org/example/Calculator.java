package org.example;

public class Calculator {

    public int sum (int a, int b){ return a + b; }

    public long sum (long a, long b){ return a + b; }

    public short sum (short a, short b){ return (short)(a + b); }

    public float sum (float a, float b) { return a + b; }

    public double sum (double a, double b){ return a + b; }

    public int difference (int a, int b){ return a - b; }

    public long difference (long a, long b){ return a - b; }

    public short difference (short a, short b){ return (short)(a - b); }

    public float difference (float a, float b) { return a - b; }

    public double difference (double a, double b){ return a - b; }

}

